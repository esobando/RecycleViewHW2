public class Spacecraft
{
    String name, propellent;
    int image;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPropellent() {
        return propellent;
    }

    public void setPropellent(String propellent) {
        this.propellent = propellent;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }
}
